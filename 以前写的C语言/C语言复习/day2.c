/*
1.基本数据类型：是无可再分的基本单位。也就是最基本的数据组成部分。
2.构造数据类型：是根据已定义或者说已存在的数据类型构建的一个新的方法。
	这个方法叫做构造数据类型，后面还有构造参数与之相关。
3.指针类型：它是一种特殊的又有重要意义的数据类型，是C家族独有的一种数据类型。
	它就像是一个标签，或者门牌号码，标记电脑中的内存地址。具体用法后面再说。
4.空类型：空类型呢，上节课讲到的返回值，如果调用这个函数（方法），你并不需要它返回函数值，
	那么它的值就可以定义为空类型。其类型的说明符往往用void表示，表示不返回值。具体用法后面再说。
5.变量与常量：如 int a = 12；这个语句，int是关键字，也是定义数据类型的说明符，定义的是一个整数的数据类型。
	a为标识符，本身也是个变量，变量就是可变的量，就像是个盒子，这个盒子被定义了放的东西的内容，也给盒子起了名字。
	而放进盒子的东西就是我们说的常量，在这里123就是放进盒子里的常量。 
*/ 

